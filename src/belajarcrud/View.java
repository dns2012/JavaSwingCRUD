    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String nim = textNim.getText();
        
        if(nim.equals("")) {
            JOptionPane.showMessageDialog(this, "pilih data yang akan dihapus");
        } else {
            Integer nimInt = null;
            
            try {
                nimInt = Integer.parseInt(nim);
            } catch(Exception e) {
                JOptionPane.showMessageDialog(this, "masukkan nim dengan integer");
            }
            
            try {
                Connection connection = Database.getConnection();
                String query = "DELETE FROM tbl_mahasiswa WHERE nim=?";
                PreparedStatement statement = (PreparedStatement) connection.prepareStatement(query);
                statement.setInt(1, nimInt);
                
                statement.executeUpdate();
                JOptionPane.showMessageDialog(this, "data berhasil dihapus");
                btnResetActionPerformed(evt);
                loadTabelMahasiswa();
            } catch (SQLException e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(this, "data gagal dihapus");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tabelMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMahasiswaMouseClicked
        int i = tabelMahasiswa.getSelectedRow();
        if(i == -1) {
            JOptionPane.showMessageDialog(this, "pilih data");
        } else {
            textNim.setText(tabelModel.getValueAt(i, 0)+"");
            textNama.setText((String)tabelModel.getValueAt(i, 1));
            textJurusan.setText((String)tabelModel.getValueAt(i, 2));
            
            textNim.setEditable(false);
        }
    }//GEN-LAST:event_tabelMahasiswaMouseClicked

