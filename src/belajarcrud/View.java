    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String nim = textNim.getText();
        String nama = textNama.getText();
        String jurusan = textJurusan.getText();
        
        if(nim.equals("")) {
            JOptionPane.showMessageDialog(this, "kolom nim tidak boleh kosong");
        } else if(nama.equals("")) {
            JOptionPane.showMessageDialog(this, "kolom nama tidak boleh kosong");
        } else if(jurusan.equals("")) {
            JOptionPane.showMessageDialog(this, "kolom jurusan tidak boleh kosong");
        } else {
            Integer nimInt = null;
            
            try {
                nimInt = Integer.parseInt(nim);
            } catch(Exception e) {
                JOptionPane.showMessageDialog(this, "masukkan nim dengan integer");
            }
            
            try {
                Connection connection = Database.getConnection();
                String query = "UPDATE tbl_mahasiswa SET nama=?, jurusan=? WHERE nim=?";
                PreparedStatement statement = (PreparedStatement) connection.prepareStatement(query);
                statement.setString(1, nama);
                statement.setString(2, jurusan);
                statement.setInt(3, nimInt);
                
                statement.executeUpdate();
                JOptionPane.showMessageDialog(this, "data berhasil diubah");
                btnResetActionPerformed(evt);
                loadTabelMahasiswa();
            } catch (SQLException e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(this, "data gagal diubah");
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

